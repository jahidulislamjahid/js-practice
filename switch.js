// switch jumps to the right properties ignoring all other possible propeties 
var color = "blue"

switch (color){
    case "red":
        console.log("the color is red");
        break;
    case "yellow":
        console.log("the color is yellow");
        break;
    case "black":
        console.log("the color is black");
        break;
    case "blue":
        console.log("the color is blue");
        break;
    case "pink":
        console.log("the color is pink");
        break;
    default:
        console.log("the color is not blue");
}